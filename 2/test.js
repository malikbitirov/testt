const missing = require('./index.js');
// Тесты для проверки функции missing()

// Тест 1: Пропущено одно значение в середине массива
console.log("Test 1:", missing([1, 4, 3])); // Ожидаемый результат: 2

// Тест 2: Пропущено одно значение в начале массива
console.log("Test 2:", missing([5, 1, 4, 2])); // Ожидаемый результат: 3

// Тест 3: Все числа присутствуют в массиве
console.log("Test 3:", missing([1, 2, 3, 4])); // Ожидаемый результат: undefined

// Тест 4: Пропущено одно значение в конце массива
console.log("Test 4:", missing([1, 2, 3, 5])); // Ожидаемый результат: 4

// Тест 5: Массив содержит только одно число
console.log("Test 5:", missing([5])); // Ожидаемый результат: undefined

// Тест 6: Массив пустой
console.log("Test 6:", missing([])); // Ожидаемый результат: undefined

// Тест 7: Массив содержит отрицательные числа
console.log("Test 7:", missing([-3, -2, 0, 1])); // Ожидаемый результат: -1
